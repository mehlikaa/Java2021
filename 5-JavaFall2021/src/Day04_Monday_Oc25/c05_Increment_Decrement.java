package Day04_Monday_Oc25;

public class c05_Increment_Decrement {

	public static void main(String[] args) {
		int sayi1=10;
		System.out.println(sayi1+=5);     // 15
		System.out.println(sayi1);        // sayi1 artik 15
		System.out.println(sayi1+12);    // = yok ve atama yok sayi1 hala 15
		sayi1++;
		System.out.println(sayi1);
	}

}
