package CB01_EN0204JavaTypeCasting;

public class CB01_Variables {
	public static void main(String[] args) {
		
		// ascii koduna donusturdu.
		
		char aLetter='u';
		short byteNumber = (short) aLetter;
		System.out.println(byteNumber);

		
		int byteNumber2 = (int)aLetter;
		System.out.println(byteNumber2);
		
		double byteNumber3 = (double)aLetter;
		System.out.println(byteNumber3);
		
	}
	
	
}
